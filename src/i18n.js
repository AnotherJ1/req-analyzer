function detectNavigatorLocale() {
  const nav = String(navigator.language || "").toLowerCase();
  const exact = i18n[nav];
  if (exact) return nav;
  const match = Object.keys(i18n).find((key) => key.toLowerCase() === nav);
  if (match) return match;
  const prefix = nav.replace(/[-_].*$/, "");
  const best = Object.keys(i18n).find((key) => key.toLowerCase().startsWith(prefix));
  return best || DEFAULT_LOCALE;
}

const I18nStore = {
  _lang: DEFAULT_LOCALE,
  _ready: false,
  _listeners: [],

  get lang() { return this._lang; },

  setLang(locale, opts = {}) {
    if (!i18n[locale]) locale = DEFAULT_LOCALE;
    if (this._lang === locale && this._ready) return;
    this._lang = locale;
    this._ready = true;
    for (const fn of this._listeners) fn(locale);
    if (opts.persist !== false) {
      try { chrome?.storage?.local?.set({ language: locale }).catch(() => {}); } catch {}
    }
  },

  onChange(fn) {
    this._listeners.push(fn);
    if (this._ready) fn(this._lang);
  },
};

globalThis.__i18nStore = I18nStore;
globalThis.SUPPORTED_LOCALES = SUPPORTED_LOCALES;
globalThis.detectNavigatorLocale = detectNavigatorLocale;

function t(key) {
  return i18n[I18nStore._lang]?.[key] || i18n[DEFAULT_LOCALE]?.[key] || key;
}
globalThis.t = t;

function formatMessage(template, values) {
  return String(template || "").replace(/\{(\w+)\}/g, (_, k) => values[k] ?? "");
}
globalThis.formatMessage = formatMessage;

function applyI18n(root = document) {
  root.documentElement?.setAttribute?.("lang", I18nStore._lang);
  for (const node of root.querySelectorAll("[data-i18n]")) {
    node.textContent = t(node.dataset.i18n);
  }
  for (const node of root.querySelectorAll("[data-i18n-placeholder]")) {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  }
  for (const node of root.querySelectorAll("[data-i18n-title]")) {
    node.title = t(node.dataset.i18nTitle);
  }
  for (const node of root.querySelectorAll("[data-i18n-aria-label]")) {
    node.setAttribute("aria-label", t(node.dataset.i18nAriaLabel));
  }
}
globalThis.applyI18n = applyI18n;

async function initI18n() {
  try {
    const stored = await chrome?.storage?.local?.get(["language"]);
    const locale = stored?.language;
    // 已保存过则用保存值；首次打开（无存储值）跟随浏览器语言，之后用户可自由切换
    I18nStore.setLang(i18n[locale] ? locale : detectNavigatorLocale(), { persist: false });
  } catch {
    I18nStore.setLang(detectNavigatorLocale(), { persist: false });
  }
  applyI18n();
}
globalThis.initI18n = initI18n;